function Get-RelPath([string]$fullPath) {
  $base = (Get-Location).Path
  $rel = $fullPath.Substring($base.Length + 1)
  return ($rel -replace "\\", "/")
}

$dirs = @(Get-ChildItem -Path "output" -Directory -Filter "*_steps" | Sort-Object Name)
