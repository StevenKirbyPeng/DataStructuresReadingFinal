$js = @"
<script>
function show(k){
  const img=document.getElementById('img_'+k);
  const c=document.getElementById('counter_'+k);
  if(!img || !frames[k] || frames[k].length===0) return;
  img.src=frames[k][indexMap[k]];
  if(c) c.textContent=(indexMap[k]+1)+' / '+frames[k].length;
}
function nextFrame(k){ indexMap[k]=(indexMap[k]+1)%frames[k].length; show(k); }
function prevFrame(k){ indexMap[k]=(indexMap[k]-1+frames[k].length)%frames[k].length; show(k); }
function getSpeed(k){ const s=document.getElementById('speed_'+k); return parseInt((s&&s.value)||1000); }
function togglePlay(k){
  if(timers[k]){ clearInterval(timers[k]); timers[k]=null; return; }
  timers[k]=setInterval(()=>nextFrame(k), getSpeed(k));
}
function restartTimer(k){
  if(timers[k]){ clearInterval(timers[k]); timers[k]=setInterval(()=>nextFrame(k), getSpeed(k)); }
}
window.addEventListener('load',()=>Object.keys(frames).forEach(show));
</script>
"@

